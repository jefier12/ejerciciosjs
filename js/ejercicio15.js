// 15. Escribir un programa que calcule la longitud y el área de una circunferencia

let radio = prompt("Ingrese el radio de la circunferencia:");
let PI = 3.1416;

let longitud = 2 * PI * radio;
let area = PI * radio * radio;

alert("La longitud de la circunferencia es: " + longitud);
alert("El área de la circunferencia es: " + area);
